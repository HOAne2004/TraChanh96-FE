// --- LOGIC XỬ LÝ MENU ---

// Kiểm tra item có đang active không
const isActive = (path) => {
  if (path === '/admin') return route.path === '/admin' // Dashboard chính xác
  return route.path.startsWith(path) // Các menu con
}

// Kiểm tra xem menu cha có nên được highlight không (khi con active)
const isParentActive = (item) => {
  if (!item.subItems) return isActive(item.path)
  return item.subItems.some((sub) => isActive(sub.path))
}

const toggleSidebar = () => {
  isSidebarOpen.value = !isSidebarOpen.value
}

const toggleSubMenu = (title) => {
  if (expandedMenus.value.includes(title)) {
    expandedMenus.value = expandedMenus.value.filter((t) => t !== title)
  } else {
    expandedMenus.value.push(title)
  }
}

const navigateTo = (path) => {
  router.push(path)
  // Trên mobile thì đóng sidebar khi click
  if (window.innerWidth < 1024) {
    isSidebarOpen.value = false
  }
}

// Tự động mở menu cha khi route thay đổi (hoặc F5)
watch(
  () => route.path,
  () => {
    menuItems.forEach((item) => {
      if (item.subItems && item.subItems.some((sub) => isActive(sub.path))) {
        if (!expandedMenus.value.includes(item.title)) {
          expandedMenus.value.push(item.title)
        }
      }
    })
  },
  { immediate: true },
)
</script>

